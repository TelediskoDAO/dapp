<script lang="ts">
  import Chip, { Text } from "@smui/chips";
  import Card, { Content, Actions } from "@smui/card";
  import Button, { Label } from "@smui/button";
  import LayoutGrid, { Cell } from "@smui/layout-grid";
  import CircularProgress from "@smui/circular-progress";

  import { RESOLUTION_STATES } from "../../state/resolutions/new";
  import { title } from "../../state/runtime";
  import { get } from "../../net/ipfs";
  import { onMount } from "svelte";
  import { graphQLClient } from "../../net/graphQl";
  import { getResolutionsQuery } from "../../graphql/get-resolutions.query";

  let resolutions;

  onMount(async () => {
    const { resolutionMockTests } = await graphQLClient.request(
      getResolutionsQuery
    );

    resolutions = await Promise.all(
      resolutionMockTests.map(async (item): Promise<any> => {
        const ipfsData = await get(item.ipfsDataURI);
        return {
          ...ipfsData,
          resolutionId: item.id,
        };
      })
    );
  });

  title.set("Resolutions");
</script>

<section>
  <div class="header">
    <h1>Resolutions</h1>
    <Button variant="raised" href="#/resolutions/new">
      <Label>Create resolution</Label>
    </Button>
  </div>
  {#if !resolutions}
    <CircularProgress style="height: 32px; width: 32px;" indeterminate />
  {/if}
  {#if resolutions?.length > 0}
    <LayoutGrid>
      {#each resolutions as resolution}
        <Cell span={4}>
          <Card>
            <Content
              >{resolution.title}
              <Chip chip><Text>{resolution.state}</Text></Chip></Content
            >
            <Actions>
              <Button
                variant="raised"
                href={`#/resolutions/${resolution.resolutionId}${
                  resolution.state === RESOLUTION_STATES.PRE_DRAFT
                    ? "/edit"
                    : ""
                }`}
              >
                <Label
                  >{resolution.state === RESOLUTION_STATES.PRE_DRAFT
                    ? "Edit"
                    : "View"}</Label
                >
              </Button>
            </Actions>
          </Card>
        </Cell>
      {/each}
    </LayoutGrid>
  {/if}
</section>

<style>
  .header {
    display: flex;
    align-items: center;
    justify-content: space-between;
  }
</style>
